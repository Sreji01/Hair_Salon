    private void jButtonKreirajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonKreirajActionPerformed
        try {
            musterija = ClientController.getInstance().kreirajMusterija();
            JOptionPane.showMessageDialog(this, "Sistem je kreirao musteriju!", "Obavestenje!", JOptionPane.INFORMATION_MESSAGE);
            omoguciPolja();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da kreira musteriju!", "Greska!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonKreirajActionPerformed

    private void jButtonZapamtiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonZapamtiActionPerformed
        try {
            if (jTextFieldIme.getText().equals("") || jTextFieldPrezime.getText().equals("") || jTextFieldEmail.getText().equals("")
                    || jTextFieldTelefon.getText().equals("") || jComboBoxTipMusterije.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!", "Upozorenje!", JOptionPane.WARNING_MESSAGE);
                return;
            }
            musterija.setIme(jTextFieldIme.getText());
            musterija.setPrezime(jTextFieldPrezime.getText());
            musterija.setTelefon(jTextFieldTelefon.getText());
            musterija.setEmail(jTextFieldEmail.getText());
            musterija.setTipMusterije((TipMusterije) jComboBoxTipMusterije.getSelectedItem());
            ClientController.getInstance().promeniMusterija(musterija);
            if (pmf != null) {
                pmf.popuniTabeluMusterije();
            }
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio musteriju!", "Obavestenje!", JOptionPane.INFORMATION_MESSAGE);
            if (jButtonKreiraj.isVisible()) {
                isprazniPolja();
                onemoguciPolja();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da zapamti musteriju!", "Greska!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonZapamtiActionPerformed

