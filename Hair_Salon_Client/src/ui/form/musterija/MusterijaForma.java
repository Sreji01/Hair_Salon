    private void popuniComboBoxTipovimaMusterija() throws Exception {
        List<TipMusterije> tipoviMusterija = ClientController.getInstance().vratiListuSviTipMusterije();
        jComboBoxTipMusterije.setModel(new DefaultComboBoxModel<>(tipoviMusterija.toArray()));
        jComboBoxTipMusterije.setSelectedItem(null);
    }

    public void onemoguciPolja() {
        jTextFieldIme.setEnabled(false);
        jTextFieldPrezime.setEnabled(false);
        jTextFieldEmail.setEnabled(false);
        jTextFieldTelefon.setEnabled(false);
        jComboBoxTipMusterije.setEnabled(false);
        jButtonZapamti.setEnabled(false);
        jButtonKreiraj.setEnabled(true);
    }

    public void omoguciPolja() {
        jTextFieldIme.setEnabled(true);
        jTextFieldPrezime.setEnabled(true);
        jTextFieldEmail.setEnabled(true);
        jTextFieldTelefon.setEnabled(true);
        jComboBoxTipMusterije.setEnabled(true);
        jButtonZapamti.setEnabled(true);
        jButtonKreiraj.setEnabled(false);
    }

    private void isprazniPolja() {
        jTextFieldIme.setText("");
        jTextFieldPrezime.setText("");
        jTextFieldTelefon.setText("");
        jTextFieldEmail.setText("");
        jComboBoxTipMusterije.setSelectedItem(null);
    }

    public void kreairanje() {
        jButtonKreiraj.setVisible(true);
        jButtonZapamti.setVisible(true);
        jButtonObrisi.setVisible(false);
        jButtonKreiraj.setEnabled(true);
        jButtonZapamti.setEnabled(false);
    }

    void detalji() {
        jButtonKreiraj.setVisible(false);
        jButtonZapamti.setVisible(false);
        jButtonObrisi.setVisible(false);
        jTextFieldIme.setEditable(false);
        jTextFieldPrezime.setEditable(false);
        jTextFieldEmail.setEditable(false);
        jTextFieldTelefon.setEditable(false);
        jComboBoxTipMusterije.setEnabled(false);
        jTextFieldIme.setText(musterija.getIme());
        jTextFieldPrezime.setText(musterija.getPrezime());
        jTextFieldEmail.setText(musterija.getEmail());
        jTextFieldTelefon.setText(musterija.getTelefon());
        jComboBoxTipMusterije.setSelectedItem(musterija.getTipMusterije());
    }

    void izmena() {
        jButtonKreiraj.setVisible(false);
        jButtonZapamti.setVisible(true);
        jButtonObrisi.setVisible(false);
        jTextFieldIme.setText(musterija.getIme());
        jTextFieldPrezime.setText(musterija.getPrezime());
        jTextFieldEmail.setText(musterija.getEmail());
        jTextFieldTelefon.setText(musterija.getTelefon());
        jComboBoxTipMusterije.setSelectedItem(musterija.getTipMusterije());
    }

    void brisanje() {
        jButtonKreiraj.setVisible(false);
        jButtonZapamti.setVisible(false);
        jButtonObrisi.setVisible(true);
        jTextFieldIme.setEditable(false);
        jTextFieldPrezime.setEditable(false);
        jTextFieldEmail.setEditable(false);
        jTextFieldTelefon.setEditable(false);
        jComboBoxTipMusterije.setEnabled(false);
        jTextFieldIme.setText(musterija.getIme());
        jTextFieldPrezime.setText(musterija.getPrezime());
        jTextFieldEmail.setText(musterija.getEmail());
        jTextFieldTelefon.setText(musterija.getTelefon());
        jComboBoxTipMusterije.setSelectedItem(musterija.getTipMusterije());
    }
