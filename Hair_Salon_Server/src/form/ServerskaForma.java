    private void jButtonPotvrdiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPotvrdiActionPerformed
        try {
            maksBrojKlijenata = Integer.parseInt(jTextFieldMaksBrojKlijenata.getText());
            Controller.getInstance().setMaksBrojKlijenata(maksBrojKlijenata);
            jButtonPokreniServer.setEnabled(true);
            jButtonPotvrdi.setEnabled(false);
            jTextFieldMaksBrojKlijenata.setEnabled(false);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Broj nije u dobrom formatu!", "Upozorenje", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButtonPotvrdiActionPerformed

