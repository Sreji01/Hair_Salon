    private void jButtonPokreniServerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPokreniServerActionPerformed
        if (server == null || !server.isAlive()) {
            server = new Server();
            server.start();
            jLabelStatusServera.setText("Server je pokrenut!");
            jButtonZasutaviServer.setEnabled(true);
            jButtonPokreniServer.setEnabled(false);
        }
    }//GEN-LAST:event_jButtonPokreniServerActionPerformed

    private void jButtonZasutaviServerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonZasutaviServerActionPerformed
        if (server.getServerSocket() != null && server.getServerSocket().isBound()) {
            try {
                server.getServerSocket().close();
                jLabelStatusServera.setText("Server je ugasen!");
                jButtonZasutaviServer.setEnabled(false);
                jButtonPokreniServer.setEnabled(true);
            } catch (IOException ex) {
            }
        }
    }//GEN-LAST:event_jButtonZasutaviServerActionPerformed

